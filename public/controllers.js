angular.module('myApp').controller('loginController',
  ['$scope', '$location', 'AuthService',
  function ($scope, $location, AuthService) {

    $scope.login = function () {

      // initial values
      $scope.error = false;
      $scope.disabled = true;

      // call login from service
      AuthService.login($scope.loginForm.username, $scope.loginForm.password)
        // handle success
        .then(function () {
          $location.path('/');
          $scope.disabled = false;
          $scope.loginForm = {};
        })
        // handle error
        .catch(function () {
          $scope.error = true;
          $scope.errorMessage = "Invalid username and/or password";
          $scope.disabled = false;
          $scope.loginForm = {};
        });

    };

}]);

angular.module('myApp').controller('logoutController',
  ['$scope', '$location', 'AuthService',
  function ($scope, $location, AuthService) {

    $scope.logout = function () {

      // call logout from service
      AuthService.logout()
        .then(function () {
          $location.path('/login');
        });

    };

}]);

angular.module('myApp').controller('diaryPostController', [ '$scope', '$http', function($scope, $http) {
		$scope.payload = '';
		$scope.result = '';
		$scope.showResult = false;

		$scope.onSubmit = function() {
			$http.post('/analyze', { payload: $scope.payload })
			.then(function successCallback(response) {
				$scope.result = JSON.stringify(response.data.entities)+'\n'+JSON.stringify(response.data.docEmotions);					
				//console.log(response);
				$scope.showResult = true;
			}, function errorCallback(response) {
				$scope.result = 'Oops! Something went wrong :(';
				$scope.showResult = true;
			});
		};
	}]);

